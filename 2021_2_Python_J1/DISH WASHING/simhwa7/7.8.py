# 7.8 사용자로부터 숫자 1보다 크고 10보다 작은 값 n을 입력으로 받아서 다음과 같이
# 뱀의 몸통처럼 증가하는 이차원 배열을 출력하는 뱀행렬을 생성하여라.
# 이 행렬의 특징은 n이 5일때 5x5크기의 2차원 배열을 생성하는데,
# 이때 홀수 번째 행이 숫자가 증가하는 방향이 될 경우 짝수 번째 행은 숫자가 감소하는 형태를 가지는 특징이 있다.
# 이 문제를 반드시 for 반복문과 리스트 슬라이싱을 사용하여 풀어보아라.

