list1 =[i + 1 for i in range(n ** 2)]
# 입력 받은 수를 제곱하여 리스트에 숫자 추가
k = 0

for i in range(n):
    list2 = list1[k:n * (i + 1)]
    k = k + n
    for j in range(n):
        if i % 2 == 0:
          print('{0:3d}'.format(list2[j]), end = " ")
          # 3칸 띄고 출력하기
        else:
            print('{0:3d}'.format(list2[-j-1]), end = " ")
